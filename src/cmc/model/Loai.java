package cmc.model;

public class Loai {
  private int ma;
  private String ten;
  private String mota;

  public int getMa() {
    return ma;
  }

  public void setMa(int ma) {
    this.ma = ma;
  }

  public String getTen() {
    return ten;
  }

  public void setTen(String ten) {
    this.ten = ten;
  }

  public String getMota() {
    return mota;
  }

  public void setMota(String mota) {
    this.mota = mota;
  }

}
